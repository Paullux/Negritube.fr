        <p class="description">
          Feu mon grand ami Guy CORDOVAL [11-08-1929 / 04-05-2003] auteur-compositeur-interprète Guadeloupéen qu'on ne présente plus grâce à sa chanson "La complainte noire" publiée au milieu des années 60, fut en son temps, un artiste hors normes, hors série; n'étant ni un suiveur ni un meneur de mode tout en étant de son époque. Beaucoup de ses chansons interrogent sur les situations du moment. Avec des textes poétiques et des harmonies riches, il mettait souvent le doigt sur des sujets que les artistes populaires n'osaient aborder. L'un des paradoxes de Guy CORDOVAL réside dans le fait qu'il évoquait des sujets comme dans "La complainte noire"  en disant "Mandé mandé Bondyé..." ou alors en écrivant l'"Avé Maria Antillais", alors qu'il n'était pas religieux et ne fréquentait pas les églises. L'idée de l'Avé Maria Antillais lui est venue quand il a écouté l'Avé Maria de SCHUBERT et l'Avé Maria de GOUNOD. il s'est dit pourquoi il n'y aurait pas un Avé Maria Antillais.  Cette oeuvre écrite au mitan des années 60, relate la vie antillaise de ces années-là. Elle n'a jamais été publiée; c'est pourquoi je me fais le plaisir de partager avec vous l'AVé MARIA ANTILLAIS.
